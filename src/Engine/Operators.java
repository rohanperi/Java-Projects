package Engine;

public class Operators 
{
	
	public static void main(String[] args)
	{		
		
		if ((false) | (true))
		{
			System.out.println("WOW! It's working!");
		}
		
		char theGrade = 'A';
		
		switch (theGrade)
		{
		case 'B':
			System.out.println("GTFO");
			
		case 'A':
			System.out.println("Excellent! A level 4!");
			
		default:
			System.out.println("Now get the hell out of my sight!");
		}
		
		
		if ((true) | (false))
		{
			System.out.println("It works, Codecademy BLESSUP!");
		}
		
		
		
	}
	
}
